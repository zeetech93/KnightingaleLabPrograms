package SwitchCase;

public class SwitchCaseDemo {

	public static void main(String[] args) {
int i=200;
		
		switch(i)
		{		
			case 100:{
				System.out.println("the i value is 100");
				break;
			}
			case 200:{
				System.out.println("the i value is 200");
				break;
			}
			default:{
				System.out.println("no condition is satisfied....");
			}
		}
	}

}
